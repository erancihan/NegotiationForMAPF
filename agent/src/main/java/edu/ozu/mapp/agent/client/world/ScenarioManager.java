    private void InitializeWorld()
    {
//        world = new World();
        world = new WorldManager();
        world.SetOnLoopingStop(() -> generate_scenario_btn.setEnabled(true));
        world.SetLogDrawCallback(
            (data, log) -> {
                // update canvas
                try {
                    scenario_info_pane.setText(
                        data
                            .keySet()
                            .stream()
                            .map(key -> {
                                switch (key)
                                {
                                    case "world_state":
                                        return key + ": " + Globals.WORLD_STATES.getOrDefault(Integer.parseInt(data.get(key)), Globals.WorldState.NONE).toString() + "\n";
                                    case "negotiation_count":
                                        return "Current Active Negotiation Count:" + data.get(key) + "\n";
                                    case "move_action_count":   // IGNORE REDUNDANT
                                    case "time_tick":           // IGNORE MISLEADING/UNRELATED INFO
                                        return "";
                                    default:
                                        return key + ": " + data.get(key) + "\n";
                                }
                            })
                            .collect(Collectors.joining("")) +
                        "\n-------------\n" +
                        log
                            .stream()
                            .map(item -> String.format("%-23s", item[1].toString()) + " " + item[0].toString())
                            .collect(Collectors.joining("\n"))
                    );
                } catch (Exception e) {
                    e.printStackTrace();
                    System.exit(1);
                }
            });
        world.SetCurrentStateChangeCallback((state) -> label_current_state.setText(state));
        world.SetCanvasUpdateCallback(() -> scenario_canvas.Update(true));

        scenario_canvas.SetWorldRef(world);
        scenario_canvas.SetWorldData(world_data);
        scenario_canvas.SetAgentsData(agents_data);
        scenario_canvas.Init();
    }

